//Print numbers from 1 to 100 using for loop and while loop 


#include <iostream>
using namespace std;
int main()
{
    // For loop
    int i=0;
    for (int i = 1; i <=100 ; i++)
    {
        cout<<i<<endl ;
    }

    //while loop
 
    while (i<=100)
    {
        cout<<i<<endl;
        i++;
    }
    
    return 0;
}

